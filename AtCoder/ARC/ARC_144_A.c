#include <stdio.h>
int main(void){
    int N;
    scanf("%d", &N);
    printf("%d\n", 2 * N); //M
    if (N % 4) {
        printf("%d", N % 4);
    }
    for (int i = 0; i < N / 4; i++) {
        putchar('4');
    }
    putchar('\n');
    return 0;
}
